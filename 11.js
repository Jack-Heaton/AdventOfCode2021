function runSteps( octo, steps ) {

	for( let i = 0; i < steps; i++) {
  	octo = runStep(octo);
  }
  
  return octo;

} 

function runStep(octo) {

  //Increase all by 1
  octo.keys.forEach(k => {
    octo[k].p++
  });

  //Flash!
  octo.stepFlash = false;
  octo = flash(octo);

  //Reset
  octo.keys.forEach(k => {
    if (octo[k].p > 9) {
      octo[k].flashed = false;
      octo[k].p = 0;
    }
  });

  return octo;

}

function flash(octo) {

  //Flash any octopus that's powered up and hasn't flashed
  octo.keys.forEach(k => {
    const o = octo[k];
    if (o.p > 9 && !o.flashed) {
      //Flash!
      octo[k].flashed = true;
      octo[k].flashCount++;
      //Power up adjacents
      const adjactent = [
      octo[`${o.x-1}-${o.y-1}`],
      octo[`${o.x-1}-${o.y}`],
      octo[`${o.x-1}-${o.y+1}`],
      octo[`${o.x}-${o.y-1}`],
      octo[`${o.x}-${o.y+1}`],
      octo[`${o.x+1}-${o.y-1}`],
      octo[`${o.x+1}-${o.y}`],
      octo[`${o.x+1}-${o.y+1}`],
      ].filter( a => a )
      	.forEach( a => {
        	octo[`${a.x}-${a.y}`].p++;
        });
      //Set globals
      octo.flashCount++;
      octo.stepFlash = true;
    }
  });

  return octo;

}


function parseData(d) {
  const swarm = d.split("\n")
    .reduce((c, l, i) => {
      l.split("").forEach((s, j) => {
        c[`${i}-${j}`] = {
          p: parseInt(s),
          x: i,
          y: j,
          flashCount: 0,
        };
      });
      return c;
    }, {})

  return {
    ...swarm,
    keys: Object.keys(swarm),
    flashCounter: 0,
    step: 0,
    stepFlash: false,
  };
}

