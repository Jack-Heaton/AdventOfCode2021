function processCode(code) {

  return code.map(l => processLine(l))
  .reduce((c,l) => c + (l.score || 0), 0 );

};


function processLine(line) {

  while (!line.status) {
    line = processPairs(line);
  }

  return line;

}


function processPairs(line) {

  if (line.findIndex(s => !s.checked) === -1 ) {
    line.status = line.status || "valid";
    return line;
  }

  if (line.filter(s => !s.checked && !delims()[s.char]).length === 0) {
    line.status = line.status || "incomplete";
    return line;
  }

  const closingPair = line.findIndex((s, i) => {
    const nextClosing = line.find((s, k) => k > i && !s.checked);
    return nextClosing && !s.checked && delims()[s.char] && delims()[s.char] === nextClosing.char;
  });

  if (closingPair === -1) {
    line.status = "invalid";
    
    line.score = scores()[line.find( s => !s.checked && !delims()[s.char]).char];
    
    return line;
  }

  line[closingPair].checked = true;
  line[line.findIndex((s, k) => k > closingPair && !s.checked)].checked = true;

  return line;

}

function scores() {
  return {
    ">": 25137,
    "}": 1197,
    "]": 57,
    ")": 3,
  };
}

function delims() {
  return {
    "<": ">",
    "{": "}",
    "[": "]",
    "(": ")",
  };
}


function parseLines(d) {
  return d.split("\n")
    .map(l => {
      return l.split("")
        .reduce((c, s, i) => {
          c.push({
            char: s,
          });
          return c;
        }, []);

    });
}

